namespace Pal3.Command.SceCommands
{
    [SceCommand(142, "设置特效位置，" +
                     "参数：X，Y，Z")]
    public class EffectSetPositionCommand : ICommand
    {
        public EffectSetPositionCommand(float x, float y, float z)
        {
            X = x;
            Y = y;
            Z = z;
        }

        public float X { get; }
        public float Y { get; }
        public float Z { get; }
    }
}